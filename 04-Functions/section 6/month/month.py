def check_month(m):
    if m == '1':
        print('1 is January')
    elif m == '2':
        print('2 is February')
    elif m == '3':
        print('3 is March')
    elif m == '4':
        print('4 is April')
    elif m == '5':
        print('5 is May')
    elif m == '6':
        print('6 is June')
    elif m == '7':
        print('7 is July')
    elif m == '8':
        print('8 is August')
    elif m == '9':
        print('9 is September')
    elif m == '10':
        print('10 is October')
    elif m == '11':
        print('11 is November')
    elif m == '12':
        print('12 is December')
    else:
        print('Invalid data')